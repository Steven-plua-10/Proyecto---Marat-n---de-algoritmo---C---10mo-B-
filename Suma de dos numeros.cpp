#include <iostream>
using namespace std;

int main() {
    cout << "=== Suma de dos numeros ===" << endl;
    double num1, num2;
    cout << "Ingresa el primer numero: ";
    cin >> num1;
    cout << "Ingresa el segundo numero: ";
    cin >> num2;
    cout << "La suma de " << num1 << " y " << num2 << " es " << num1 + num2 << endl;
    return 0;
}
